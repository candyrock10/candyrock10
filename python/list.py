# creating a list

a = [1, 2, 5, 4, 6, 9]
print(a)   # this will print our list
print(a[2])  # this will print value at index 2 which is 5
a[1]=9   # this will change the value of INDEX 1 FROM 2 TO 9
print(a)
b=[1,"utkarsh",2.5] # list with different data types 
print(b)
b[2]="candy" # value of index 2 is changed to candy in list b
